async function report(args) {
  const metricsPath = path.resolve(args.metricsFile);
  const data = JSON.parse(fs.readFileSync(metricsPath, "utf8"));
  const format = args.format || "markdown";

  const pipelineDiagram = generateMermaidPipeline(data);
  const moduleDiagram = generateMermaidModuleGraph(data);

  if (format === "html") {
    const html = generateHtml(data);
    const outputPath = args.outputPath
      ? path.resolve(args.outputPath).replace(/\.md$/, ".html")
      : null;
    if (outputPath) {
      fs.writeFileSync(outputPath, html, "utf8");
    }
    return { format: "html", content: html, outputPath };
  }

  const md = generateMarkdown(data, pipelineDiagram, moduleDiagram);

  if (args.outputPath) {
    fs.writeFileSync(path.resolve(args.outputPath), md, "utf8");
  }

  return { format, content: md, outputPath: args.outputPath || null };
}

module.exports = { report, generateMermaidPipeline, generateMermaidModuleGraph };
