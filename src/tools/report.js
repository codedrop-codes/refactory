function generateMermaidPipelineRaw(data) {
  const modules = data.decomposed.modules || [];
  const clean = modules.filter(m => m.clean).length;
  return `flowchart TD
    A["${path.basename(data.original.file)}\\n${data.original.lines.toLocaleString()} lines | ${data.original.functions} functions"] --> B["ANALYZE\\nHealth: ${(data.health?.overall || 0).toFixed(1)}"]
    B --> C["CHARACTERIZE\\n${data.characterize?.exportCount || 0} exports snapshotted"]
    C --> D["PLAN\\n${modules.length} modules via ${data.plan?.provider || 'LLM'}"]
    D --> E["EXTRACT\\n${modules.length} modules generated"]
    E --> F["FIX-IMPORTS\\nMechanical path rewrite"]
    F --> G["VERIFY\\n${clean}/${modules.length} clean"]
    G --> H["SCORE: ${data.refactoryScore}"]
    style A fill:#e74c3c,color:#fff
    style H fill:#${data.refactoryScore >= 0.9 ? '2ecc71' : data.refactoryScore >= 0.7 ? 'f39c12' : 'e74c3c'},color:#fff`;
}

function generateMermaidModuleGraphRaw(data) {
  const modules = data.decomposed.modules || [];
  if (modules.length === 0) return "";
  let graph = `flowchart LR\n    ORIG["${path.basename(data.original.file)}\\n${data.original.lines}L"] --> INDEX["index.js\\nre-export"]\n`;
  for (const m of modules) {
    const color = m.clean ? "#2ecc71" : "#e74c3c";
    const id = m.file.replace(/[^a-zA-Z0-9]/g, "_");
    graph += `    INDEX --> ${id}["${m.clean ? "OK" : "FAIL"} ${m.file}\\n${m.lines}L"]\n`;
    graph += `    style ${id} fill:${color},color:#fff\n`;
  }
  return graph;
}

