"use strict";
const fs = require("node:fs");
const path = require("node:path");

function generateMermaidPipeline(data) {
  const modules = data.decomposed.modules || [];
  const clean = modules.filter(m => m.clean).length;
  const failed = modules.length - clean;

  return `\`\`\`mermaid
flowchart TD
    A["📄 ${path.basename(data.original.file)}<br/>${data.original.lines.toLocaleString()} lines | ${data.original.functions} functions"] --> B["🔍 ANALYZE<br/>Health: ${(data.health?.overall || 0).toFixed(1)}"]
    B --> C["📸 CHARACTERIZE<br/>${data.characterize?.exportCount || 0} exports snapshotted"]
    C --> D["📐 PLAN<br/>${modules.length} modules via ${data.plan?.provider || 'LLM'}"]
    D --> E["⚙️ EXTRACT<br/>${modules.length} modules generated"]
    E --> F["🔧 FIX-IMPORTS<br/>Mechanical path rewrite"]
    F --> G["✅ VERIFY<br/>${clean}/${modules.length} clean"]
    G --> H["📊 SCORE: ${data.refactoryScore}"]

    style A fill:#e74c3c,color:#fff
    style H fill:#${data.refactoryScore >= 0.9 ? '2ecc71' : data.refactoryScore >= 0.7 ? 'f39c12' : 'e74c3c'},color:#fff
\`\`\``;
}

function generateMermaidModuleGraph(data) {
  const modules = data.decomposed.modules || [];
  if (modules.length === 0) return "";

  let graph = "```mermaid\nflowchart LR\n";
  graph += `    ORIG["${path.basename(data.original.file)}<br/>${data.original.lines}L"] --> INDEX["index.js<br/>re-export"]\n`;
  for (const m of modules) {
    const status = m.clean ? "✅" : "❌";
    const color = m.clean ? "#2ecc71" : "#e74c3c";
    const id = m.file.replace(/[^a-zA-Z0-9]/g, "_");
    graph += `    INDEX --> ${id}["${status} ${m.file}<br/>${m.lines}L"]\n`;
    graph += `    style ${id} fill:${color},color:#fff\n`;
  }
  graph += "```";
  return graph;
}

